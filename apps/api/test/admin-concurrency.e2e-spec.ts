/**
 * Admin concurrency + credential-security integration tests (wave-15, P-4 load-bearing).
 *
 * GUARD: suite is skipped when TEST_DATABASE_URL is unset or DB unreachable.
 *
 * ── RACE-SAFE last-admin guard (CONCURRENCY TEST — P-4 load-bearing) ────────
 * Test CONC-1: Two concurrent transactions each attempt to deactivate a DIFFERENT
 * one of the last two admins. The advisory-lock guard must ensure EXACTLY ONE
 * succeeds — the second is rejected with 409. At least one admin always remains.
 *
 * This test PROVES that pg_advisory_xact_lock(ADMIN_GUARD_LOCK_KEY) closes the
 * write-skew window that a plain `count(*) FOR UPDATE` cannot close.
 *
 * The test calls the REAL UserManagementService.deactivateAsActor (not inline SQL).
 * Fault-killing property: if the production guard regressed to count(*)-FOR-UPDATE,
 * two concurrent deactivateAsActor calls on DIFFERENT last-two admins would BOTH
 * read count=2 (one for each, excluding themselves → 1 remaining each), BOTH
 * proceed, and both succeed → 0 admins remain. The "exactly one fulfilled + ≥1
 * remains" assertion would then FAIL, catching the regression.
 *
 * ── CREDENTIAL NEVER LEAKS (SECURITY TEST — P-4 load-bearing) ───────────────
 * Test SEC-1: The credential is absent from the audit row written during
 * createConnection. (Asserts on the real audit_log_entries row.)
 *
 * Test SEC-2: The credential is absent from a FORCED-error path.
 * Trigger a DB error (constraint violation on displayName unique index) WHILE
 * a credential is present in the input. Assert the thrown error does NOT contain
 * the plaintext credential.
 *
 * Test SEC-3: round-trip encrypt → decrypt (also in unit tests; repeated here
 * to confirm the CI CREDENTIALS_ENC_KEY env var is set and works end-to-end).
 *
 * Test SEC-4: GET /admin/integrations (via DataSourceAdminService.listConnections)
 * never returns the plaintext credential — only hasCredential boolean.
 *
 * ── UUID NAMESPACE ───────────────────────────────────────────────────────────
 * Wave-15 prefix '00000015-*' — disjoint from all prior suites:
 *   outreach-gate:    10000000-60000000
 *   pipeline-gate:    00000012-*
 *   recordkeeping:    00000014-*
 *
 * ── TEARDOWN POLICY (WORM-SAFE) ──────────────────────────────────────────────
 * All tests call real service methods that commit audit_log_entries rows with
 * actor_user_id pointing at the seeded users. Deleting those users would trigger
 * the ON DELETE SET NULL FK on audit_log_entries.actor_user_id, which fires an
 * UPDATE on an immutable table → audit_log_block_mutation() raises P0001.
 *
 * LESSON from wave-12/13/14: DO NOT hard-delete users (or any row FK-referenced
 * by an immutable audit_log_entries row). Instead:
 *   • All user fixtures are RETAINED across runs.
 *   • Per-test state resets (deactivated_at, role) are done via UPDATE.
 *   • data_source_connections (not FK-referenced by audit_log_entries) are
 *     hard-deleted in finally blocks as before.
 *   • Dynamic (Date.now) email fixtures generated by SEC tests accumulate across
 *     runs but are isolated to the wave-15 namespace and do not affect other suites.
 */

