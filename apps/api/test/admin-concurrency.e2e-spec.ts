    /** Resolve a role id by name. */
    async function getRoleId(name: string): Promise<string> {
      const { sql } = await import('drizzle-orm');
      const rows = await db.execute<{ id: string }>(sql`
        SELECT id FROM roles WHERE name = ${name} LIMIT 1
      `);
      const id = (rows.rows ?? rows)[0]?.id;
      if (!id) throw new Error(`Role '${name}' not found in roles table`);
      return id;
    }

    /**
     * Insert a minimal test user with a known role. Returns the users.id UUID.
     *
     * WORM-SAFE: uses ON CONFLICT (email) DO NOTHING so re-runs that hit the same
     * email (e.g. fixed wave-15 fixture emails) are idempotent. Dynamic Date.now()
     * emails used by CONC tests generate new rows each run — those are retained
     * (never deleted) because they accumulate audit_log_entries references.
     *
     * NOTE: returning id after ON CONFLICT DO NOTHING requires a two-step approach
     * (INSERT then SELECT) because ON CONFLICT DO NOTHING yields no RETURNING row
     * on the conflict branch. We INSERT first; if it returns a row we use it,
     * otherwise we SELECT the existing row.
     */
    async function createTestUser(email: string, roleName: string): Promise<string> {
      const { sql } = await import('drizzle-orm');
      const roleId = await getRoleId(roleName);
      // Try insert; returns the new row or nothing if email already exists.
      const insertRows = await db.execute<{ id: string }>(sql`
        INSERT INTO users (supertokens_user_id, email, role_id)
        VALUES (gen_random_uuid()::text, ${email}, ${roleId}::uuid)
        ON CONFLICT (email) DO NOTHING
        RETURNING id
      `);
      const inserted = (insertRows.rows ?? insertRows)[0]?.id;
      if (inserted) return inserted;
      // Email already existed — fetch the existing user id.
      const selectRows = await db.execute<{ id: string }>(sql`
        SELECT id FROM users WHERE email = ${email} LIMIT 1
      `);
      const existing = (selectRows.rows ?? selectRows)[0]?.id;
      if (!existing) throw new Error(`User with email ${email} not found after conflict`);
      return existing;
    }

    /**
     * Reset a test user's deactivated_at to NULL and optionally restore its role.
     * Used INSTEAD of deleteTestUser — avoids triggering the WORM audit FK.
     */
    async function resetTestUser(userId: string, roleName?: string): Promise<void> {
      const { sql } = await import('drizzle-orm');
      if (roleName) {
        const roleId = await getRoleId(roleName);
        await db.execute(sql`
          UPDATE users SET deactivated_at = NULL, role_id = ${roleId}::uuid
          WHERE id = ${userId}::uuid
        `);
      } else {
        await db.execute(sql`
          UPDATE users SET deactivated_at = NULL WHERE id = ${userId}::uuid
        `);
      }
    }

